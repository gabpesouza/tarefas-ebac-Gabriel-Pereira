package br.com.ebac.mod8;

public class CalculoMedia {

	public static void main(String[] args) {
		
		double nota1 = 6.0;
		double nota2 = 6.5;
		double nota3 = 9.3;
		double nota4 = 8.2;
		
		double media = (nota1 + nota2 + nota3 + nota4) / 4;
		
		System.out.printf("Valor da media final: %2f", media);
		
	}
		
}
